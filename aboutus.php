<center>From a Hiligaynon word for Mr. Barbecue, Mang Inasal was established on December 12, 2003 in Iloilo City by Edgar “Injap” Sia II. Apart from the delicious, craveable taste of its Chicken Inasal that has won the hearts of Filipinos, Mang Inasal was also the first to offer unlimited rice.<br><br>

Over the years, the brand has garnered numerous awards as a fast-growing local food company, an outstanding quick service restaurant (QSR), and an excellent Filipino franchise.<br><br>

All these efforts enabled Mang Inasal to land the top 4th spot among QSRs in the country and as the number one inasal destination in Manila, according to Southeast Asia’s leading travel inspiration platform, TripZilla.<br><br>

Mang Inasal has maintained a strong portfolio that includes the best-tasting 2-in-1 sa Laki, Tagos ang Ihaw-Sarap Chicken Inasal, Pork Barbecue, Extra Creamy Halo-Halo, and Palabok, continuously delighting its growing customer base whether via dine-in, takeout, or delivery.<br>
